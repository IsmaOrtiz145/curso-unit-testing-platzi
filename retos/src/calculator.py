"""Reto de la clase 03"""
def suma(a, b):
    "Retorma la suma de dos numeros"
    return a + b

def resta(a, b):
    """Retorma la resta de dos numeros"""
    return a - b

def division(a, b):
    """Retorma la division de dos numeros"""
    return a / b

def multiplicacion(a, b):
    """Retorma la multiplicacion de dos numeros"""
    return a * b

def potencia_num(a, b):
    return a ** b